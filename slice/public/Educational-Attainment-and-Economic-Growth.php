                      <p><font face="Arial"><font face="Arial" size="-1">In order for Oklahoma to maximize its economic potential in ensuing years, it must focus on educating its work force and fostering a culture that values completion of college, continuing education, and training throughout each person's life and career.</font></font></p>

                      <p><font face="Arial"><font face="Arial" size="-1">The relentless speed of technological development, deregulation of long-standing monopolistic industries, and a global playing field will determine the future of national and local economies. Each community's ability to compete successfully in this environment will increasingly depend on human capital factors such as educational attainment of work force, according to Mark Zandi, Chief Economist for Regional Financial Associates.</font></font></p>

                      <p><font face="Arial"><font face="Arial" size="-1">Craig Knutson, Chief Economist for Southwestern Bell, reiterated this message at a recent meeting of the Citizens' Task Force on Taxation. In the emerging economy, industries capable of generating the most capital will be those that depend on intellectual capacity to add value to raw materials. Mr. Knutson stated that we are shifting from a 'brawn'-based economy of raw materials and labor to a 'brain'-based economy fueled by a highly educated work force.</font></font></p>

