                        <p>Oklahoma has fewer college graduates per capita than the national average and most surrounding states. More importantly, we are falling further behind, especially in key fields of science and technology. This trend must be reversed to put Oklahoma into a competitive position in today's knowledge-based economy.</p>

                        <p>Oklahoma must generate more college graduates to enrich its labor force and make itself attractive to high-wage information companies. We can do this by:</p>

