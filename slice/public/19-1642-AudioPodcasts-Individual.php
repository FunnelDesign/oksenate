            <div class="bPost__text">
              <p>Sen. Julie Daniels and Rep. Mark Lepak will attend a White House event on Thursday to hear President Trump speak about protecting First Amendment rights on college campuses. He is expected to sign an executive order tying the receipt of federal research funding to universities’ actions to ensure freedom of expression on campus. The two legislators received the invitation because of their work on a bill to protect freedom of expression on Oklahoma campuses.</p>
              <p>Senate Bill 361, by Daniels, R-Bartlesville, and Lepak, R-Claremore, would require Oklahoma’s public universities, colleges and the career technology institutions to take action to protect students’ rights to free speech on campus. Oklahoma is among several states taking legislative action to protect First Amendment rights on campus. Arkansas passed a similar bill earlier this year. Another has been adopted in Kentucky. Campus free speech legislation is being introduced in Texas and South Carolina. Arizona and Tennessee already have such statutes.</p>
              <p>“I authored SB 361 so that Oklahoma college students may engage in the free exchange of a wide spectrum of ideas – which is absolutely critical to the academic and intellectual integrity of a college education. This legislation will also protect university administrators against those who seek to silence any expression they deem offensive,” Daniels said. “There is no diversity without diversity of thought, opinion and expression. There is no freedom without free speech.”</p>
              <p>SB 361 clarifies that colleges and universities cannot prohibit free speech in public areas, and that those public areas of campus will be available for scheduled, organized or spontaneous free expression by members of the campus community. Administrators may set reasonable time, place and manner restrictions on free speech activities. The bill clarifies what it means to disrupt freedom of expression and that speech not protected by the First Amendment will not be protected on campus. University administrations will be required to orient students, faculty, staff and campus law enforcement on its free speech policies.</p>
              <p>“I’m very excited to be invited to the White House along with Senator Daniels. Freedom of speech is essential to our democracy and must be embraced as part of a higher education,” Lepak said.
                The measure was approved in the Senate by a vote 36 to 9 and will next be presented by Lepak in a House committee.</p>

