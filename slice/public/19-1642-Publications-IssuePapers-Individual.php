             <p>In the Budget Reconciliation Act of 1993, Congress provided income tax incentives to businesses located on Indian reservations. The way the term "Indian reservations" was used in the bill caused confusion as to Congressional intent. To resolve this issue, Congress further defined "reservations" and portions of Oklahoma that qualify as reservations in the Taxpayer Relief Act of 1997. With this latest action, Congress has made certain its intent for application of these tax incentives in Oklahoma.</p>
              <ul>
                <li>The Omnibus Budget Reconciliation Act of 1993 added two federal income tax incentives designed to stimulate economic development on Indian reservations throughout the United States. <br> <br>

                  - The accelerated depreciation for property on Indian reservations allowed businesses to depreciate
                  their capital expenses more rapidly than other properties, which could reduce a business's tax
                  liability earlier and in turn improve the present value of its after-tax income. <br> <br>

                  - The tax credit for Indian employees allowed businesses to use a portion of the wages paid and
                  health insurance benefits provided to Indian employees to directly reduce their tax liability and in
                  turn increase after-tax income.
                </li>
                
                <li>Due to Oklahoma's unique history, it has a very high Native American population but no current "Indian reservations". The Taxpayer Relief Act of 1997 clarifies the definition of the term "Indian reservation" in Oklahoma as "lands which are within the jurisdictional area of an Oklahoma Indian tribe (as determined by the Secretary of the Interior)".</li>
                
                <li>To further clarify Congressional intent, Congressman Wes Watkins constructed a "bright-line test" in which the eligible lands are determined to be "lands within boundaries of the last treaties with the Oklahoma tribes".</li>
                <li>Currently, approximately two-thirds of Oklahoma qualifies for the depreciation and employment tax credit incentives. The areas that do not qualify are historically identified as No Man's Land, Old Greer County, the Unassigned Lands, and the Cherokee Outlet.</li>

              </ul>
