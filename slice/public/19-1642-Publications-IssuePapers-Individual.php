              <p>The passage of the Taxpayer Relief Act of 1997 on August 5, clarified the term "Indian reservation" as it pertains to Oklahoma as "lands which are within the jurisdictional area of an Oklahoma Indian tribe". For purposes of Congressional intent, the House Ways and Means Committee defines an Indian reservations in Oklahoma as "lands within boundaries of the last treaties with the Oklahoma tribes." This definition satisfied Committee Chairman Bill Archer in that it narrowed the scope of the qualifying lands by eliminating the Unassigned Lands.</p>
              <p>The Internal Revenue Code of 1986 (as amended) now provides for additional accelerated depreciation of property placed on an Indian reservation (as defined above). To be eligible, the property must:</p>
              <p>be used by the taxpayer predominantly in the active conduct of a trade or business within an Indian reservation on a regular basis.</p>
              <p>not be used or located outside the Indian reservation on a regular basis.</p>
              <p>not be used in conducting or housing class I, II or III gaming as defined in the Indian Regulatory Act.</p>
              <p>not be residential rental property.</p>
              <p>not be owned by a person who is required to use the "alternative depreciation system".</p>
              <h4>background</h4>
              
              <p>In 1992 Senator John McCain, R-Arizona, drafted a tax bill with the assistance of the Navajo Nation to make it more attractive for businesses to hire Native Americans and to invest in reservation land. This bill was ultimately vetoed by President Bush.</p>
              <p>The 1993 budget-reconciliation bill included "empowerment zones" aimed at low-income inner cities and poor rural areas. McCain fought to make Indians eligible for the program as well arguing that Indians are among the poorest Americans. Oklahoma Indians were often excluded from these types of assistance packages because they are commonly aimed at Indian "reservations". Oklahoma has no official reservations, because land that was once reservation land has since either been allotted for settlement or given to individuals or tribes. Then Oklahoma Senator David Boren insisted that Oklahoma Indians be included in the tax incentives.</p>
              <p>The Omnibus Reconciliation Act of 1993 was signed into law on August 10, 1993. It contained two provisions to stimulate economic development on Indian reservations by providing assistance to companies investing in and conducting business on such lands between the years 1994 and 2003. These tax incentives were similar to those targeted at other areas of economic depression, known as empowerment zones. First, the Act authorized Accelerated Depreciation Values for Property on Indian Reservations and second, it outlined the Indian Employment Credit.</p>
              <p>The accelerated depreciation for property on Indian reservations allowed businesses to depreciate their capital expenses more rapidly than other properties, which could reduce a business's tax liability earlier and in turn improve the present value of its after-tax income.</p>


